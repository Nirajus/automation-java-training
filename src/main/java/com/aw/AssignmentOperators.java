package com.aw;

public class AssignmentOperators {
public static void main(String[] args) {
    int num = 10;
    System.out.println("Initial value: " + num);
    num+= 5;
    System.out.println("Value after addition: " + num);
    num-= 2;
    System.out.println("Value after subtraction: " + num);
    num*= 3;
    System.out.println("Value after multiplication: " + num);
    num/= 4;
    System.out.println("Value after division: " + num);
   
    

}
}
